<#
.SYNOPSIS
Performs write operations on monitor templates in the WhatsUp Gold monitor library.

.DESCRIPTION
The Set-WUGMonitorTemplate function handles write operations for monitor templates:
- Apply/import monitor templates:  PATCH  /api/v1/monitors/-/config/template
- Batch monitor library operations: PATCH  /api/v1/monitors/-
- Remove all monitor assignments:   DELETE /api/v1/monitors/-/assignments/-

.PARAMETER ApplyTemplates
Switch to apply or import monitor templates.
Endpoint: PATCH /api/v1/monitors/-/config/template

.PARAMETER BatchMonitors
Switch to perform batch operations on monitors in the library.
Endpoint: PATCH /api/v1/monitors/-

.PARAMETER UnassignAll
Switch to remove all monitor assignments that match a query.
Endpoint: DELETE /api/v1/monitors/-/assignments/-

.PARAMETER Body
JSON body for ApplyTemplates and BatchMonitors operations.

.PARAMETER Search
Optional search filter for UnassignAll to limit which assignments are removed.

.EXAMPLE
# Apply monitor templates
$body = @{ templates = @(@{ name = "HTTP"; type = "active" }) } | ConvertTo-Json -Depth 5
Set-WUGMonitorTemplate -ApplyTemplates -Body $body

.EXAMPLE
# Batch update monitors in the library
$body = @{ items = @(@{ monitorId = "123"; enabled = $true }) } | ConvertTo-Json -Depth 5
Set-WUGMonitorTemplate -BatchMonitors -Body $body

.EXAMPLE
# Remove all monitor assignments
Set-WUGMonitorTemplate -UnassignAll

.NOTES
Author: Jason Alberino (jason@wug.ninja)
Reference: https://docs.ipswitch.com/NM/WhatsUpGold2024/02_Guides/rest_api/index.html#tag/Monitor-Templates
#>
function Set-WUGMonitorTemplate {
    [CmdletBinding(SupportsShouldProcess = $true)]
    param(
        [Parameter(Mandatory = $true, ParameterSetName = 'ApplyTemplates')]
        [switch]$ApplyTemplates,

        [Parameter(Mandatory = $true, ParameterSetName = 'BatchMonitors')]
        [switch]$BatchMonitors,

        [Parameter(Mandatory = $true, ParameterSetName = 'UnassignAll')]
        [switch]$UnassignAll,

        [Parameter(Mandatory = $true, ParameterSetName = 'ApplyTemplates')]
        [Parameter(Mandatory = $true, ParameterSetName = 'BatchMonitors')]
        [string]$Body,

        [Parameter(ParameterSetName = 'UnassignAll')]
        [string]$Search
    )

    begin {
        Write-Debug "Starting Set-WUGMonitorTemplate function. ParameterSet: $($PSCmdlet.ParameterSetName)"
        $baseUri = "${global:WhatsUpServerBaseURI}/api/v1/monitors"
    }

    process {
        switch ($PSCmdlet.ParameterSetName) {

            'ApplyTemplates' {
                $uri = "${baseUri}/-/config/template"
                Write-Debug "Applying monitor templates. URI: $uri"

                if (-not $PSCmdlet.ShouldProcess('Monitor templates', 'Apply')) { return }

                try {
                    $result = Get-WUGAPIResponse -Uri $uri -Method 'PATCH' -Body $Body
                    if ($result.data) { return $result.data }
                    return $result
                }
                catch {
                    Write-Error "Error applying monitor templates: $_"
                }
            }

            'BatchMonitors' {
                $uri = "${baseUri}/-"
                Write-Debug "Batch monitor operations. URI: $uri"

                if (-not $PSCmdlet.ShouldProcess('Monitor library', 'Batch update')) { return }

                try {
                    $result = Get-WUGAPIResponse -Uri $uri -Method 'PATCH' -Body $Body
                    if ($result.data) { return $result.data }
                    return $result
                }
                catch {
                    Write-Error "Error performing batch monitor operations: $_"
                }
            }

            'UnassignAll' {
                $queryParams = @()
                if ($Search) { $queryParams += "search=$([uri]::EscapeDataString($Search))" }
                $query = if ($queryParams.Count -gt 0) { "?" + ($queryParams -join "&") } else { "" }
                $uri = "${baseUri}/-/assignments/-${query}"
                Write-Debug "Removing all monitor assignments. URI: $uri"

                if (-not $PSCmdlet.ShouldProcess('All monitor assignments', 'Remove')) { return }

                try {
                    $result = Get-WUGAPIResponse -Uri $uri -Method 'DELETE'
                    if ($result.data) { return $result.data }
                    return $result
                }
                catch {
                    Write-Error "Error removing monitor assignments: $_"
                }
            }
        }
    }

    end {
        Write-Debug "Completed Set-WUGMonitorTemplate function."
    }
}


<#
.SYNOPSIS
    Generates an interactive HTML dashboard report for Hyper-V environments.
.DESCRIPTION
    Orchestration script that connects to one or more Hyper-V hosts via CIM sessions,
    collects VM details (CPU, memory, disk, network, snapshots, replication), and
    produces a searchable, sortable Bootstrap Table HTML dashboard. Supports
    WinRM and DCOM transport protocols. Output includes both a JSON data file
    and a self-contained HTML report.
.PARAMETER HypervHosts
    One or more Hyper-V host names or IP addresses. If omitted, prompts interactively.
    Accepts an array of strings (e.g. from Get-Content).
.PARAMETER HypervCredential
    A PSCredential object for authenticating to the Hyper-V hosts. If omitted,
    prompts interactively via Get-Credential.
.PARAMETER UseDCOM
    Use DCOM transport instead of the default WinRM (WSMan) for CIM sessions.
    Useful for hosts that do not have WinRM enabled.
.EXAMPLE
    .\Get-HypervDashboard.ps1

    Prompts for Hyper-V host(s) and credentials, then generates the dashboard.
.EXAMPLE
    .\Get-HypervDashboard.ps1 -HypervHosts "hyperv01.lab.local" -HypervCredential (Get-Credential)

    Generates a dashboard for a single Hyper-V host.
.EXAMPLE
    .\Get-HypervDashboard.ps1 -HypervHosts "hv01","hv02" -UseDCOM

    Connects to multiple hosts via DCOM and generates a combined dashboard.
.EXAMPLE
    $hosts = Get-Content .\hosts.txt
    .\Get-HypervDashboard.ps1 -HypervHosts $hosts -HypervCredential $cred

    Reads host names from a file and generates the dashboard for all hosts.
.OUTPUTS
    System.Void
    Produces a JSON file (hyperv_dashboard.json) and an HTML dashboard
    (Hyperv-Dashboard.html) in the system temp directory, then opens the HTML in the default browser.
.NOTES
    Author  : jason@wug.ninja
    Version : 1.0.0
    Date    : 2025-07-15
    Requires: PowerShell 5.1+, Hyper-V PowerShell module, HypervHelpers.ps1 in the same directory.
.LINK
    https://github.com/jayyx2/WhatsUpGoldPS
#>

param (
    [Parameter(Mandatory = $false)]
    [string[]]$HypervHosts,

    [Parameter(Mandatory = $false)]
    [pscredential]$HypervCredential,

    [Parameter(Mandatory = $false)]
    [switch]$UseDCOM
)

# --- Configuration -----------------------------------------------------------
$helpersPath = Join-Path $PSScriptRoot "HypervHelpers.ps1"
if (Test-Path $helpersPath) {
    . $helpersPath
}
else {
    throw "HypervHelpers.ps1 not found at $helpersPath. Ensure it is in the same directory."
}

if (Get-Module -ListAvailable -Name WhatsUpGoldPS) {
    if (-not (Get-Module -Name WhatsUpGoldPS)) {
        Import-Module -Name WhatsUpGoldPS
    }
}

# --- Input prompts -----------------------------------------------------------
if (-not $HypervHosts -or $HypervHosts.Count -eq 0) {
    $hostInput = Read-Host -Prompt "Enter Hyper-V host(s) (comma-separated)"
    $HypervHosts = $hostInput -split ',' | ForEach-Object { $_.Trim() } | Where-Object { $_ -ne '' }
}

if (-not $HypervHosts -or $HypervHosts.Count -eq 0) {
    throw "At least one Hyper-V host must be specified."
}

if (-not $HypervCredential) {
    $HypervCredential = Get-Credential -Message "Enter Hyper-V host credentials"
}

# Output paths
$outputDir = if ($env:TEMP) { $env:TEMP } else { "C:\temp" }
$jsonPath  = Join-Path $outputDir "hyperv_dashboard.json"
$htmlPath  = Join-Path $outputDir "Hyperv-Dashboard.html"
if (-not (Test-Path $outputDir)) { New-Item -ItemType Directory -Path $outputDir -Force | Out-Null }

# --- Connect and collect data ------------------------------------------------
$sessions = @()

foreach ($hvHost in $HypervHosts) {
    Write-Host "Connecting to $hvHost ..." -ForegroundColor Cyan
    try {
        $splat = @{ ComputerName = $hvHost; Credential = $HypervCredential }
        if ($UseDCOM) { $splat["UseDCOM"] = $true }
        $session = Connect-HypervHost @splat
        $sessions += $session
        Write-Host "  Connected to $hvHost" -ForegroundColor Green
    }
    catch {
        Write-Warning "Failed to connect to ${hvHost}: $($_.Exception.Message)"
    }
}

if ($sessions.Count -eq 0) {
    Write-Warning "No Hyper-V hosts connected. Exiting."
    return
}

Write-Host "`nCollecting VM data from $($sessions.Count) host(s)..." -ForegroundColor Cyan
$dashboardData = Get-HypervDashboard -CimSessions $sessions

if (-not $dashboardData -or $dashboardData.Count -eq 0) {
    Write-Warning "No VM data collected. Exiting."
    return
}

# --- Summary -----------------------------------------------------------------
$running = @($dashboardData | Where-Object { $_.State -eq 'Running' }).Count
$off     = @($dashboardData | Where-Object { $_.State -eq 'Off' }).Count
$other   = $dashboardData.Count - $running - $off
$hosts   = @($dashboardData | Select-Object -ExpandProperty Host -Unique).Count

Write-Host "`n--- Hyper-V Summary ---" -ForegroundColor Yellow
Write-Host "  Hosts:   $hosts"
Write-Host "  Running: $running" -ForegroundColor Green
Write-Host "  Off:     $off" -ForegroundColor Red
if ($other -gt 0) { Write-Host "  Other:   $other" -ForegroundColor DarkYellow }

# --- Generate outputs --------------------------------------------------------
$dashboardData | ConvertTo-Json -Depth 5 | Out-File $jsonPath -Force -Encoding UTF8
Write-Host "`nJSON data written to $jsonPath" -ForegroundColor Yellow

$templatePath = Join-Path $PSScriptRoot "Hyperv-Dashboard-Template.html"
Export-HypervDashboardHtml -DashboardData $dashboardData -OutputPath $htmlPath -ReportTitle "Hyper-V Dashboard" -TemplatePath $templatePath
Write-Host "HTML dashboard written to $htmlPath" -ForegroundColor Yellow

# --- Cleanup CIM sessions ----------------------------------------------------
foreach ($s in $sessions) {
    Remove-CimSession -CimSession $s -ErrorAction SilentlyContinue
}

# --- Optional: Open in browser -----------------------------------------------
$openBrowser = Read-Host -Prompt "Open dashboard in browser? (Y/N)"
if ($openBrowser -match '^[Yy]') {
    Start-Process $htmlPath
}

Write-Host "`nDone." -ForegroundColor Green


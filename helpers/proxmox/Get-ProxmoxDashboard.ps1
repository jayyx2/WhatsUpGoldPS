<#
.SYNOPSIS
    Generates an interactive HTML dashboard report for Proxmox VE environments.
.DESCRIPTION
    Orchestration script that connects to one or more Proxmox VE servers via the
    REST API, collects VM details (CPU, memory, disk, network, HA state), and
    produces a searchable, sortable Bootstrap Table HTML dashboard. Output
    includes both a JSON data file and a self-contained HTML report.
.PARAMETER ProxmoxServers
    One or more Proxmox VE server URIs (e.g. https://192.168.1.100:8006).
    If omitted, prompts interactively.
.PARAMETER Username
    Proxmox authentication username (e.g. root@pam). If omitted, prompts interactively.
.PARAMETER Password
    Proxmox authentication password. If omitted, prompts interactively as a secure string.
.PARAMETER SkipSSLCheck
    Bypass SSL certificate validation. Useful for lab environments with self-signed
    certificates.
.EXAMPLE
    .\Get-ProxmoxDashboard.ps1

    Prompts for Proxmox server(s) and credentials, then generates the dashboard.
.EXAMPLE
    .\Get-ProxmoxDashboard.ps1 -ProxmoxServers "https://192.168.1.100:8006" -Username "root@pam"

    Generates a dashboard for a single Proxmox server (prompts for password).
.EXAMPLE
    .\Get-ProxmoxDashboard.ps1 -ProxmoxServers "https://pve1:8006","https://pve2:8006" -SkipSSLCheck

    Connects to multiple Proxmox servers with SSL bypass and generates a combined dashboard.
.EXAMPLE
    .\Get-ProxmoxDashboard.ps1 -ProxmoxServers "https://pve:8006" -Username "admin@pve" -Password "MyPass"

    Generates a dashboard with explicit credentials (non-interactive).
.OUTPUTS
    System.Void
    Produces a JSON file (proxmox_dashboard.json) and an HTML dashboard
    (Proxmox-Dashboard.html) in the system temp directory, then opens the HTML in the default browser.
.NOTES
    Author  : jason@wug.ninja
    Version : 1.0.0
    Date    : 2025-07-15
    Requires: PowerShell 5.1+, network access to Proxmox VE API (port 8006), ProxmoxHelpers.ps1 in the same directory.
.LINK
    https://github.com/jayyx2/WhatsUpGoldPS
#>

param (
    [Parameter(Mandatory = $false)]
    [string[]]$ProxmoxServers,

    [Parameter(Mandatory = $false)]
    [string]$Username,

    [Parameter(Mandatory = $false)]
    [string]$Password,

    [Parameter(Mandatory = $false)]
    [switch]$SkipSSLCheck
)

# --- Configuration -----------------------------------------------------------
$helpersPath = Join-Path $PSScriptRoot "ProxmoxHelpers.ps1"
if (Test-Path $helpersPath) {
    . $helpersPath
}
else {
    throw "ProxmoxHelpers.ps1 not found at $helpersPath. Ensure it is in the same directory."
}

# Load vault functions for credential resolution
$discoveryHelpersPath = Join-Path (Split-Path $PSScriptRoot -Parent) 'discovery\DiscoveryHelpers.ps1'
if (Test-Path $discoveryHelpersPath) { . $discoveryHelpersPath }

if (Get-Module -ListAvailable -Name WhatsUpGoldPS) {
    if (-not (Get-Module -Name WhatsUpGoldPS)) {
        Import-Module -Name WhatsUpGoldPS
    }
}

# --- Input prompts -----------------------------------------------------------
if (-not $ProxmoxServers -or $ProxmoxServers.Count -eq 0) {
    $serverInput = Read-Host -Prompt "Enter Proxmox VE server(s) - e.g. https://192.168.1.100:8006 (comma-separated)"
    $ProxmoxServers = $serverInput -split ',' | ForEach-Object { $_.Trim() } | Where-Object { $_ -ne '' }
}

if (-not $ProxmoxServers -or $ProxmoxServers.Count -eq 0) {
    throw "At least one Proxmox VE server must be specified."
}

if (-not $Username -and -not $Password) {
    $vaultName = "Proxmox.$($ProxmoxServers[0]).Credential"
    $proxCred = Resolve-DiscoveryCredential -Name $vaultName -CredType PSCredential -ProviderLabel 'Proxmox' -AutoUse
    if ($proxCred) {
        $Username = $proxCred.UserName
        $bstr = [System.Runtime.InteropServices.Marshal]::SecureStringToBSTR($proxCred.Password)
        try { $Password = [System.Runtime.InteropServices.Marshal]::PtrToStringAuto($bstr) }
        finally { [System.Runtime.InteropServices.Marshal]::ZeroFreeBSTR($bstr) }
    }
}

if (-not $Username) {
    $Username = Read-Host -Prompt "Enter Proxmox username (e.g. root@pam)"
}

if (-not $Password) {
    $securePassword = Read-Host -Prompt "Enter Proxmox password" -AsSecureString
    $Password = [System.Runtime.InteropServices.Marshal]::PtrToStringAuto(
        [System.Runtime.InteropServices.Marshal]::SecureStringToBSTR($securePassword)
    )
}

# Output paths
$outputDir = Join-Path $env:LOCALAPPDATA 'WhatsUpGoldPS\DiscoveryHelpers\Output'
$jsonPath  = Join-Path $outputDir "proxmox_dashboard.json"
$htmlPath  = Join-Path $outputDir "Proxmox-Dashboard.html"
if (-not (Test-Path -LiteralPath $outputDir)) { New-Item -ItemType Directory -Path $outputDir -Force | Out-Null }

# --- Connect and collect data ------------------------------------------------
if ($SkipSSLCheck) {
    Initialize-SSLBypass
}

$allData = @()

foreach ($server in $ProxmoxServers) {
    Write-Host "Connecting to $server ..." -ForegroundColor Cyan
    try {
        $cookie = Connect-ProxmoxServer -Server $server -Username $Username -Password $Password
        Write-Host "  Authenticated to $server" -ForegroundColor Green

        $data = Get-ProxmoxDashboard -Server $server -Cookie $cookie
        if ($data) { $allData += $data }
        Write-Host "  Collected $(@($data).Count) VM(s) from $server" -ForegroundColor Green
    }
    catch {
        Write-Warning "Failed to connect to ${server}: $($_.Exception.Message)"
    }
}

if (-not $allData -or $allData.Count -eq 0) {
    Write-Warning "No VM data collected. Exiting."
    return
}

# --- Summary -----------------------------------------------------------------
$running = @($allData | Where-Object { $_.Status -eq 'running' }).Count
$stopped = @($allData | Where-Object { $_.Status -eq 'stopped' }).Count
$other   = $allData.Count - $running - $stopped
$nodes   = @($allData | Select-Object -ExpandProperty Node -Unique).Count

Write-Host "`n--- Proxmox Summary ---" -ForegroundColor Yellow
Write-Host "  Nodes:   $nodes"
Write-Host "  Running: $running" -ForegroundColor Green
Write-Host "  Stopped: $stopped" -ForegroundColor Red
if ($other -gt 0) { Write-Host "  Other:   $other" -ForegroundColor DarkYellow }

# --- Generate outputs --------------------------------------------------------
$allData | ConvertTo-Json -Depth 5 | Out-File $jsonPath -Force -Encoding UTF8
Write-Host "`nJSON data written to $jsonPath" -ForegroundColor Yellow

$templatePath = Join-Path $PSScriptRoot "Proxmox-Dashboard-Template.html"
Export-ProxmoxDashboardHtml -DashboardData $allData -OutputPath $htmlPath -ReportTitle "Proxmox VE Dashboard" -TemplatePath $templatePath
Write-Host "HTML dashboard written to $htmlPath" -ForegroundColor Yellow

# --- Optional: Open in browser -----------------------------------------------
if ($env:OS -match 'Windows') {
    $openBrowser = Read-Host -Prompt "Open dashboard in browser? (Y/N)"
    if ($openBrowser -match '^[Yy]') {
        Start-Process $htmlPath
    }
}

Write-Host "Done." -ForegroundColor Green


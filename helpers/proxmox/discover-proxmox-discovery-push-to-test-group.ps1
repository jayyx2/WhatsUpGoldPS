    Write-Host "1. Push discovery plan to WUG..." -ForegroundColor Cyan
    & $setupPath -Target $ProxmoxHost -ApiPort $ProxmoxPort -Action PushToWUG -WUGServer $wugInfo.Server -WUGCredential $wugInfo.Credential -NonInteractive
}

Write-Host "" 

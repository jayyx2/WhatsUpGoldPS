function Get-ProxmoxPlaceholderDeviceIp {
    param(
        [Parameter(Mandatory = $true)]
        [string]$DeviceKey
    )

    $idNum = $null
    if ($DeviceKey -match '^(vm|ct):(\d+)$') {
        $idNum = [int]$Matches[2]
    }
    else {
        $sum = 0
        foreach ($ch in $DeviceKey.ToCharArray()) { $sum += [int][char]$ch }
        $idNum = $sum
    }

    $idNum = [math]::Abs($idNum)
    $oct3 = [int](($idNum / 254) % 254) + 1
    $oct4 = [int]($idNum % 254) + 1
    return "100.64.${oct3}.${oct4}"
}

function Get-ProxmoxDiscoveryDevicePlan {
    param(
        [Parameter(Mandatory = $true)]
        [string]$TargetHost,

        [Parameter(Mandatory = $true)]
        [int]$Port,

        [Parameter(Mandatory = $true)]
        [string]$ApiToken
    )

    $plan = Invoke-Discovery -ProviderName 'Proxmox' -Target $TargetHost -ApiPort $Port -Credential @{ ApiToken = $ApiToken }
    $devicePlan = @{}

    foreach ($item in $plan) {
        $type = $item.Attributes['Proxmox.DeviceType']
        switch ($type) {
            'Cluster' {
                $key  = "cluster:$($item.Attributes['Proxmox.ApiHost'])"
                $name = $item.Attributes['Proxmox.ApiHost']
                $ip   = $item.Attributes['Proxmox.ApiHost']
            }
            'Node' {
                $name = $item.Attributes['Proxmox.NodeName']
                $key  = "node:$name"
                $ip   = $item.Attributes['Proxmox.NodeIP']
            }
            'VM' {
                $vmid = $item.Attributes['Proxmox.VMID']
                $key  = "vm:$vmid"
                $name = $item.Attributes['Proxmox.VMName']
                $ip   = $item.Attributes['Proxmox.VMIP']
            }
            'CT' {
                $ctid = $item.Attributes['Proxmox.VMID']
                $key  = "ct:$ctid"
                $name = $item.Attributes['Proxmox.VMName']
                $ip   = $item.Attributes['Proxmox.VMIP']
            }
            default { continue }
        }

        if (-not $devicePlan.ContainsKey($key)) {
            $devicePlan[$key] = [PSCustomObject]@{
                Key  = $key
                Type = $type
                Name = $name
                IP   = $ip
            }
        }
    }

    return $devicePlan
}

Write-Host "=== Proxmox Discovery Push Test Group ===" -ForegroundColor Cyan
Write-Host "Proxmox: $ProxmoxHost`:$ProxmoxPort" -ForegroundColor White
Write-Host "Group  : $GroupName" -ForegroundColor White

$proxmoxToken = Resolve-DiscoveryCredential -Name "Proxmox.$ProxmoxHost.Token" -CredType BearerToken -ProviderLabel 'Proxmox' -NonInteractive

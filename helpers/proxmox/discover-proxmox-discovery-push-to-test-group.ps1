    GroupId         = $groupId
    ExpectedDevices = $devicePlan.Count
    ResolvedDevices = $resolved.Count
    MissingDevices  = $missing.Count
    SampleResolved  = @($resolved | Select-Object -First 8)
    SampleMissing   = @($missing | Select-Object -First 8)
} | ConvertTo-Json -Depth 6

function Resolve-DependencyPaths {
    <#
    .SYNOPSIS
        Rewrites CDN URLs in HTML to local file:/// paths if the dependency folder exists.
    .DESCRIPTION
        Checks for a 'dependency' folder under helpers\reports\. If it exists and contains
        the required files, replaces CDN URLs with local file:/// URIs so the dashboard
        works fully offline. If the folder doesn't exist, returns the HTML unchanged.
    .PARAMETER Html
        The HTML string to process.
    .OUTPUTS
        System.String  The HTML with URLs rewritten (or unchanged).
    #>
    [CmdletBinding()]
    param(
        [Parameter(Mandatory)]
        [string]$Html
    )

    $depFolder = Join-Path $PSScriptRoot 'dependency'
    if (-not (Test-Path $depFolder)) {
        Write-Verbose "No local dependency folder found at $depFolder — using CDN URLs."
        return $Html
    }

    $depUri = ([System.Uri](Resolve-Path $depFolder).Path).AbsoluteUri

    # Map of CDN URLs (with and without version pins) to local filenames
    $cdnMap = [ordered]@{
        # CSS
        'https://cdn.jsdelivr.net/npm/bootstrap@5.3.2/dist/css/bootstrap.min.css' = "$depUri/bootstrap.min.css"
        'https://cdn.jsdelivr.net/npm/bootstrap@5.3.0/dist/css/bootstrap.min.css' = "$depUri/bootstrap.min.css"
        'https://cdn.jsdelivr.net/npm/bootstrap/dist/css/bootstrap.min.css'       = "$depUri/bootstrap.min.css"
        'https://cdn.jsdelivr.net/npm/bootstrap-table@1.22.1/dist/bootstrap-table.min.css' = "$depUri/bootstrap-table.min.css"
        'https://cdn.jsdelivr.net/npm/bootstrap-table/dist/bootstrap-table.min.css' = "$depUri/bootstrap-table.min.css"
        'https://cdn.jsdelivr.net/npm/bootstrap-icons@1.11.1/font/bootstrap-icons.min.css' = "$depUri/bootstrap-icons.min.css"
        'https://cdn.jsdelivr.net/npm/bootstrap-icons/font/bootstrap-icons.min.css' = "$depUri/bootstrap-icons.min.css"
        # JS
        'https://cdn.jsdelivr.net/npm/jquery@3.7.1/dist/jquery.min.js'            = "$depUri/jquery.min.js"
        'https://cdn.jsdelivr.net/npm/jquery/dist/jquery.min.js'                  = "$depUri/jquery.min.js"
        'https://cdn.jsdelivr.net/npm/@popperjs/core@2.11.8/dist/umd/popper.min.js' = "$depUri/popper.min.js"
        'https://cdn.jsdelivr.net/npm/@popperjs/core/dist/umd/popper.min.js'      = "$depUri/popper.min.js"
        'https://cdn.jsdelivr.net/npm/bootstrap@5.3.2/dist/js/bootstrap.min.js'   = "$depUri/bootstrap.min.js"
        'https://cdn.jsdelivr.net/npm/bootstrap@5.3.0/dist/js/bootstrap.min.js'   = "$depUri/bootstrap.min.js"
        'https://cdn.jsdelivr.net/npm/bootstrap/dist/js/bootstrap.min.js'         = "$depUri/bootstrap.min.js"
        'https://cdn.jsdelivr.net/npm/bootstrap-table@1.22.1/dist/bootstrap-table.min.js' = "$depUri/bootstrap-table.min.js"
        'https://cdn.jsdelivr.net/npm/bootstrap-table/dist/bootstrap-table.min.js' = "$depUri/bootstrap-table.min.js"
        'https://cdn.jsdelivr.net/npm/file-saver/dist/FileSaver.min.js'           = "$depUri/FileSaver.min.js"
        'https://cdn.jsdelivr.net/npm/xlsx/dist/xlsx.full.min.js'                 = "$depUri/xlsx.full.min.js"
        'https://cdn.jsdelivr.net/npm/html2canvas@1.4.1/dist/html2canvas.min.js'  = "$depUri/html2canvas.min.js"
        'https://cdn.jsdelivr.net/npm/html2canvas/dist/html2canvas.min.js'        = "$depUri/html2canvas.min.js"
    }

    foreach ($cdn in $cdnMap.GetEnumerator()) {
        $Html = $Html.Replace($cdn.Key, $cdn.Value)
    }

    Write-Verbose "Rewrote CDN URLs to local dependencies in $depFolder"
    return $Html
}


<#
.SYNOPSIS
    Tears down OCI test resources created by Setup-OCITestResources.ps1.

.DESCRIPTION
    Reads oci-test-state.json and deletes every resource in correct
    dependency order: LB -> VM -> ADB -> subnet -> security list ->
    route table -> internet gateway -> VCN.

    Safe to re-run: ignores resources that are already deleted (404).

.PARAMETER StateFile
    Path to the state file created by Setup-OCITestResources.ps1.
    Default: $env:TEMP\WhatsUpGoldPS\oci-test-state.json

.PARAMETER Force
    Skip the confirmation prompt.

.EXAMPLE
    .\Teardown-OCITestResources.ps1

.EXAMPLE
    .\Teardown-OCITestResources.ps1 -Force

.EXAMPLE
    .\Teardown-OCITestResources.ps1 -StateFile "C:\path\to\oci-test-state.json" -Force

.NOTES
    Author: Jason Alberino (jason@wug.ninja)
    Encoding: UTF-8 with BOM
#>
[CmdletBinding()]
param(
    [string]$StateFile = (Join-Path (Join-Path $env:TEMP 'WhatsUpGoldPS') 'oci-test-state.json'),
    [switch]$Force
)

Set-StrictMode -Version Latest
$ErrorActionPreference = 'Stop'
$env:OCI_CLI_SUPPRESS_FILE_PERMISSIONS_WARNING = 'True'

# ---- Locate OCI CLI ----------------------------------------------------------

$script:OciExe = $null
foreach ($candidate in @(
    (Get-Command oci -ErrorAction SilentlyContinue | Select-Object -ExpandProperty Source),
    (Join-Path $env:USERPROFILE 'bin\oci.exe'),
    (Join-Path $env:LOCALAPPDATA 'Programs\Python\*\Scripts\oci.exe'),
    'C:\ProgramData\chocolatey\bin\oci.exe'
)) {
    if ($candidate -and (Test-Path $candidate)) { $script:OciExe = $candidate; break }
}
if (-not $script:OciExe) {
    throw "OCI CLI not found. Install it: https://docs.oracle.com/en-us/iaas/Content/API/SDKDocs/cliinstall.htm"
}

# ---- Helpers -----------------------------------------------------------------

function Invoke-OciSafe {
    <# Runs OCI CLI; returns $null on 404/NotFound instead of throwing. #>
    param([string[]]$OciArgs)
    $rawOutput = & $script:OciExe @OciArgs 2>&1
    if ($LASTEXITCODE -ne 0) {
        $outStr = "$rawOutput"
        if ($outStr -match '404|NotAuthorizedOrNotFound|does not exist|not found|TERMINATED|TERMINATING') {
            return $null
        }
        throw "OCI CLI error: $rawOutput"
    }
    # Filter out WARNING/INFO lines from stderr that break JSON parsing
    $jsonLines = $rawOutput | Where-Object {
        $line = "$_"
        $line -notmatch '^WARNING:' -and $line -notmatch '^INFO:' -and $line -ne ''
    }
    if ($jsonLines) {
        try { return ($jsonLines -join "`n" | ConvertFrom-Json) }
        catch { return $null }
    }
}

function Write-Step   { param([string]$Msg) Write-Host "`n>> $Msg" -ForegroundColor Cyan }
function Write-Ok     { param([string]$Msg) Write-Host "  OK: $Msg" -ForegroundColor Green }
function Write-Skip   { param([string]$Msg) Write-Host "  -- $Msg (not found / already deleted)" -ForegroundColor DarkGray }
function Write-Warn   { param([string]$Msg) Write-Host "  !! $Msg" -ForegroundColor Yellow }

function Remove-OciResource {
    param([string]$Label, [string]$Id, [scriptblock]$DeleteBlock)
    if (-not $Id) { Write-Skip $Label; return }
    Write-Host "  Deleting $Label ..." -NoNewline
    try {
        & $DeleteBlock
        Write-Host " OK" -ForegroundColor Green
    }
    catch {
        $msg = "$($_.Exception.Message)"
        if ($msg -match '404|NotAuthorizedOrNotFound|does not exist|not found') {
            Write-Host " (already gone)" -ForegroundColor DarkGray
        }
        else {
            Write-Host " FAILED: $msg" -ForegroundColor Yellow
        }
    }
}

function Wait-ForTermination {
    param([string]$ResourceType, [string]$Id, [int]$TimeoutSec = 600)
    if (-not $Id) { return }
    $elapsed = 0
    while ($elapsed -lt $TimeoutSec) {
        try {
            $current = switch ($ResourceType) {
                'instance' { (Invoke-OciSafe compute, instance, get, --instance-id, $Id).data.'lifecycle-state' }
                'database' { (Invoke-OciSafe db, autonomous-database, get, --autonomous-database-id, $Id).data.'lifecycle-state' }
                'lb'       { (Invoke-OciSafe lb, load-balancer, get, --load-balancer-id, $Id).data.'lifecycle-state' }
            }
            if (-not $current -or $current -in @('TERMINATED', 'DELETED')) { return }
            if ($current -in @('TERMINATING', 'DELETING')) {
                Write-Host "  Waiting for $ResourceType -> terminated ($current)..." -ForegroundColor Gray
            }
            else {
                return  # unexpected state
            }
        }
        catch {
            return  # resource gone (404)
        }
        Start-Sleep -Seconds 15
        $elapsed += 15
    }
    Write-Warn "Timed out waiting for $ResourceType termination -- continuing."
}

# ---- Load state --------------------------------------------------------------

if (-not (Test-Path $StateFile)) {
    Write-Host "State file not found: $StateFile" -ForegroundColor Red
    Write-Host "Run Setup-OCITestResources.ps1 first, or specify -StateFile." -ForegroundColor Red
    exit 1
}

$state = Get-Content $StateFile -Raw | ConvertFrom-Json
$r = $state.Resources

Write-Host ""
Write-Host "========================================" -ForegroundColor Yellow
Write-Host "  WUG OCI Test Resource Teardown" -ForegroundColor Yellow
Write-Host "========================================" -ForegroundColor Yellow
Write-Host ""
Write-Host "State file  : $StateFile" -ForegroundColor White
Write-Host "Created at  : $($state.CreatedAt)" -ForegroundColor White
$resourceCount = ($r.PSObject.Properties | Measure-Object).Count
Write-Host "Resources   : $resourceCount items" -ForegroundColor White
Write-Host ""

if (-not $Force) {
    $confirm = Read-Host "This will permanently DELETE all OCI test resources. Type 'yes' to continue"
    if ($confirm -ne 'yes') {
        Write-Host "Aborted." -ForegroundColor Red
        exit 0
    }
}

# Helper to safely get a property that may not exist under StrictMode
function Get-ResourceId {
    param([string]$Name)
    try { $r.PSObject.Properties[$Name].Value } catch { $null }
}

# ---- 1. Load Balancer --------------------------------------------------------

$lbId = Get-ResourceId 'LoadBalancerId'
Write-Step "Deleting Load Balancer"
Remove-OciResource "Load Balancer" $lbId {
    Invoke-OciSafe lb, load-balancer, delete, `
        --load-balancer-id, $lbId, `
        --force | Out-Null
}
if ($lbId) {
    Wait-ForTermination -ResourceType lb -Id $lbId -TimeoutSec 300
}

# ---- 2. Compute Instance -----------------------------------------------------

$vmId = Get-ResourceId 'VmMicroId'
Write-Step "Terminating Compute Instance"
Remove-OciResource "VM Micro" $vmId {
    Invoke-OciSafe compute, instance, terminate, `
        --instance-id, $vmId, `
        --preserve-boot-volume, 'false', `
        --force | Out-Null
}
if ($vmId) {
    Wait-ForTermination -ResourceType instance -Id $vmId -TimeoutSec 600
}
Write-Ok "Compute instances terminated"

# ---- 3. Autonomous Database ---------------------------------------------------

$adbId = Get-ResourceId 'AutonomousDbId'
Write-Step "Deleting Autonomous Database"
Remove-OciResource "Autonomous Database" $adbId {
    Invoke-OciSafe db, autonomous-database, delete, `
        --autonomous-database-id, $adbId, `
        --force | Out-Null
}
if ($adbId) {
    Wait-ForTermination -ResourceType database -Id $adbId -TimeoutSec 600
}
Write-Ok "Autonomous Database terminated"

# ---- 4. Networking (reverse dependency order) ---------------------------------

Write-Step "Deleting Networking resources"

$subnetId = Get-ResourceId 'SubnetId'
$slId     = Get-ResourceId 'SecurityListId'
$rtId     = Get-ResourceId 'RouteTableId'
$igwId    = Get-ResourceId 'IgwId'
$vcnId    = Get-ResourceId 'VcnId'

# Subnet first (depends on RT + SL)
Remove-OciResource "Subnet" $subnetId {
    Invoke-OciSafe network, subnet, delete, `
        --subnet-id, $subnetId, --force | Out-Null
    Start-Sleep -Seconds 5
}

# Security List
Remove-OciResource "Security List" $slId {
    Invoke-OciSafe network, security-list, delete, `
        --security-list-id, $slId, --force | Out-Null
}

# Route Table
Remove-OciResource "Route Table" $rtId {
    Invoke-OciSafe network, route-table, delete, `
        --rt-id, $rtId, --force | Out-Null
}

# Internet Gateway
Remove-OciResource "Internet Gateway" $igwId {
    Invoke-OciSafe network, internet-gateway, delete, `
        --ig-id, $igwId, --force | Out-Null
}

# VCN last
Remove-OciResource "VCN" $vcnId {
    Invoke-OciSafe network, vcn, delete, `
        --vcn-id, $vcnId, --force | Out-Null
}

# ---- Archive state file -------------------------------------------------------

$archivePath = $StateFile -replace '\.json$', ("-torn-down-{0}.json" -f (Get-Date -Format 'yyyyMMdd-HHmmss'))
Move-Item -Path $StateFile -Destination $archivePath
Write-Ok "State file archived to: $archivePath"

# ---- Done --------------------------------------------------------------------

Write-Host ""
Write-Host "========================================" -ForegroundColor Green
Write-Host "  Teardown complete!" -ForegroundColor Green
Write-Host "========================================" -ForegroundColor Green
Write-Host ""

